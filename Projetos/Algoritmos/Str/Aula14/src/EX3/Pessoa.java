package EX3;

public class Pessoa {
    private String Nome;
    private int idade;
    private String sexo;
    
	public String getNome() {
		return Nome;
	}
	public int getIdade() {
		return idade;
	}
	public String getSexo() {
		return sexo;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public void setSexo(String sexo) {
		this.sexo = sexo;
	}
    
}
