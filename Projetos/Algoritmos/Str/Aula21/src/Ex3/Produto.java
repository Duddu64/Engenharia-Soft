package Ex3;

public class Produto {
	private double preco;
	private String nome;
	private int qtde;
	public double getPreco() {
		return preco;
	}
	public String getNome() {
		return nome;
	}
	public int getQtde() {
		return qtde;
	}
	public void setPreco(double preco) {
		this.preco = preco;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public void setQtde(int qtde) {
		this.qtde = qtde;
	}
	
	
}
